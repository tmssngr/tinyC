void main() {
	printString("&:\n");
	print(0 & 0);
	print(0 & 1);
	print(1 & 0);
	print(1 & 1);
	printString("\n|:\n");
	print(0 | 0);
	print(0 | 1);
	print(1 | 0);
	print(1 | 1);
	printString("\n^:\n");
	print(0 ^ 0);
	print(0 ^ 2);
	print(1 ^ 0);
	print(1 ^ 2);
	printString("\n&&:\n");
	print(0 && 0);
	print(0 && 2);
	print(1 && 0);
	print(1 && 2);
	printString("\n||:\n");
	print(0 || 0);
	print(0 || 2);
	print(1 || 0);
	print(1 || 2);
	printString("\nmisc:\n");
	print(0b1010 & 0b0110 | 0b0001);
	print(1 == 2 || 2 < 3);
	print(1 == 2 && 2 < 3);
}
